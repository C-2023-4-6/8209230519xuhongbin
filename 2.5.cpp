#include<iostream>
using namespace std;
int main() {
	char a;
	int num = 0;
	int space = 0;
	int letter = 0;
	int other = 0;
	cout << "请输入一行字符：" << endl;
	while ((a = getchar()) && a!='\n') {
		if (a >= '0'&&a <= '9') {
			num++;
		}
		else if (a == ' ') 
		{
			space++;
		}
		else if ((a >= 'a'&&a <= 'z') || (a >= 'A'&&a <= 'Z')) 
		{
			letter++;
		}
		else { other++; }
	}
	cout << "数字个数为：" << num << endl;
	cout << "空格个数为：" << space << endl;
	cout << "字母个数为：" << letter << endl;
	cout << "其他个数为：" << other << endl;
	return 0;
}